#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		len;
	int		i;
	char	*array;

	if (!src)
		return (NULL);
	i = 0;
	len = (int)ft_strlen(src);
	array = (char *)malloc((len + 1) * sizeof(char));
	if (!array)
		return (NULL);
	while (src[i] != '\0')
	{
		array[i] = src[i];
		i++;
	}
	array[i] = '\0';
	return (array);
}
